	private void loadSurvey() {
		Object selectedItem = surveyComboBox.getSelectedItem();
		if (selectedItem == null) {
			throw new UnsupportedOperationException("No surveys are found. Please create a new survey.");
		}
		String surveyName = selectedItem.toString();

		SurveyDetails surveyDetails = mAdminSettingsManager.findSurveyByName(surveyName);
		Survey survey = surveyDetails.getSurvey();

		if (survey.getScaleType() == ScaleType.CONTINOUS_SCALE) {
			hundredPointScale.setSelected(true);
		} else if (survey.getScaleType() == ScaleType.FIVE_POINT_QUALITY_SCALE) {
			fivePointScale.setSelected(true);
			qualityRadioButton.setSelected(true);
			impairmentRadioButton.setSelected(false);
		} else if (survey.getScaleType() == ScaleType.FIVE_POINT_IMPAIRMENT_SCALE) {
			fivePointScale.setSelected(true);
			qualityRadioButton.setSelected(false);
			impairmentRadioButton.setSelected(true);
		} else if (survey.getScaleType() == ScaleType.BINARY_SCALE) {
			binaryScale.setSelected(true);
			BinaryScale binaryScale2 = surveyDetails.getBinaryScale();
			binaryScalePositiveTextField.setText(binaryScale2.getPositiveScale());
			binaryScaleNegativeTextField.setText(binaryScale2.getNegativeScale());
		} else if (survey.getScaleType() == ScaleType.CUSTOM_SCALE) {
			customScale.setSelected(true);
			CustomScale customScale2 = surveyDetails.getCustomScale();
			customScaleMaxTextField.setText(customScale2.getMaxScale());
			customScaleMidTextField.setText(customScale2.getMidScale());
			customScaleMinTextField.setText(customScale2.getMinScale());
		}

		Timer timer = surveyDetails.getTimer();
		String t1 = timer.getT1() + "";
		String t2 = timer.getT2() + "";
		String t3 = timer.getT3() + "";
		if (timer.getTimerType() == TimerType.ITUTIMER) {
			ituTimerRadioButton.setSelected(true);
			ituTimerT1TextField.setText(t1);
			ituTimerT2TextField.setText(t2);
			ituTimerT3TextField.setText(t3);
		} else if (timer.getTimerType() == TimerType.CUSTOMTIMER) {
			customTimerRadioButton.setSelected(true);
			customTimerT1TextField.setText(t1);
			customTimerT2TextField.setText(t2);
			customTimerT3TextField.setText(t3);

		} else if (timer.getTimerType() == TimerType.DEFAULTTIMER) {
			defaultTimerRadioButton.setSelected(true);
			defaultTimerT2TextField.setText(t2);
			defaultTimerT3TextField.setText(t3);
		}
		videoLocation.setText(survey.getVideoLocation());
	}

