	private void loadAllSurveyNames() {
		try {
			List<String> surveyNameList = mAdminSettingsManager.findAllSurveys();
			surveyComboBox.removeAllItems();
			for (String surveyName : surveyNameList) {
				surveyComboBox.addItem(surveyName);
			}
			setAsCurrentSurvey();
		} catch (Exception e) {
			surveyComboBox.removeAllItems();
			JOptionPane.showMessageDialog(null, e.getLocalizedMessage());
		}
	}

