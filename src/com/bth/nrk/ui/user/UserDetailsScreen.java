	private void validateMinimalInputs() throws Exception {
		// Validate if username is entered
		if (Integer.valueOf(ageSpinner.getValue().toString()) <= 0) {
			throw new Exception("Age should be greater than 0.");
		}
		SurveyDetails currentSurveyDetails = mUserDetailsManager.getCurrentSurveyDetails();
		if (currentSurveyDetails == null) {
			throw new Exception("Survey is not set. Please contact Administrator");
		}
	}

