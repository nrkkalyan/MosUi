	private User saveUserDetailsToDatabase() throws Exception {
		User entity = new User();
		// Age
		Integer age = Integer.valueOf(ageSpinner.getValue().toString());
		entity.setAge(age);
		// Gender
		String selectedGender = genderComboBox.getSelectedItem().toString();
		Gender gender = User.Gender.valueOf(selectedGender);
		entity.setGender(gender);
		// Skill
		String skill = skillComboBox.getSelectedItem().toString();
		entity.setSkill(skill);
		// Call the controller to save the user details
		mUserDetailsManager.saveUserDetails(entity);

		return entity;

	}
