	private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {/*
																		 * If ok
																		 * button
																		 * is
																		 * selcted
																		 * we
																		 * implemented
																		 * a
																		 * logic
																		 * to
																		 * check
																		 * whether
																		 * it
																		 * ahs
																		 * selected
																		 * admin
																		 * radio
																		 * button
																		 * or
																		 * user
																		 * radio
																		 * button
																		 */
		try {

			validateMinimalInputs();
			if (adminRadioBtn.isSelected()) {
				/*
				 * If admin radio button is selected admin login screen should
				 * be visible
				 */
				mAdminLoginScreen.setVisible(true);

			} else if (userRadioBtn.isSelected()) {
				/*
				 * Else if user radio button is selected user details screen
				 * should be set visible
				 */
				mUserDetailsScreen.setVisible(true);
			}

			this.setVisible(false);
		} catch (Exception ex) {
			/*
			 * we through an simple exception for try excution *
			 */
			JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
		}

	}// GEN-LAST:event_okButtonActionPerformed

