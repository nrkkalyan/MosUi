	private void validateMinimalInputs() throws Exception {
		/*
		 * IF neither of radio buttons(Admin || User) is selected we declare the
		 * reply as please select one of the options above.
		 */
		if (!adminRadioBtn.isSelected() && !userRadioBtn.isSelected()) {
			throw new Exception("Please select one of the options above.");
		}
	}

